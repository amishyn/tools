
# translit for rails
module Translit
  SUPPORTED_LANGUAGES = ['ua','ru']
  def self.new(str, lang = 'ua', leave_original = true)
    return if str.blank? || !SUPPORTED_LANGUAGES.include?(lang)
    result=""
    str.mb_chars.each_char{ |s| result << convert(s,lang,leave_original);}
    result
  end
  
  def self.convert(s,lang,leave_original)
    send(lang, s)||(leave_original ? s : '')
  end

  def self.ru(s)
    RU_TABLE[s]
  end
  def self.ua(s)
